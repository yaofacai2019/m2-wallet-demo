  const ordered = Object.entries(phrases).sort((a, b) => b[0].length - a[0].length);
  const translate = value => {
    if (!value || !/[\u3400-\u9fff]/.test(value)) return value;
    let result = value;
    for (const [source, target] of ordered) result = result.split(source).join(target);
    return result;
  };

  const translateElement = element => {
    if (!(element instanceof Element) || element.matches('script,style')) return;
    for (const attribute of ['title', 'placeholder', 'aria-label']) {
      if (element.hasAttribute(attribute)) element.setAttribute(attribute, translate(element.getAttribute(attribute)));
    }
    if (element instanceof HTMLInputElement && ['button', 'submit', 'reset'].includes(element.type)) {
      element.value = translate(element.value);
    }
  };

  const translateTree = root => {
    if (root.nodeType === Node.TEXT_NODE) {
      const parent = root.parentElement;
      if (parent && !parent.matches('script,style')) {
        const translated = translate(root.nodeValue);
        if (translated !== root.nodeValue) root.nodeValue = translated;
      }
      return;
    }
    if (!(root instanceof Element) && root !== document) return;
    if (root instanceof Element) translateElement(root);
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_ELEMENT | NodeFilter.SHOW_TEXT);
    let node;
    while ((node = walker.nextNode())) {
      if (node.nodeType === Node.TEXT_NODE) {
        if (!node.parentElement?.matches('script,style')) {
          const translated = translate(node.nodeValue);
          if (translated !== node.nodeValue) node.nodeValue = translated;
        }
      } else translateElement(node);
    }
  };

  const nativePrompt = window.prompt.bind(window);
  const nativeConfirm = window.confirm.bind(window);
  const nativeAlert = window.alert.bind(window);
  window.prompt = (message, initial) => nativePrompt(translate(message), initial);
  window.confirm = message => nativeConfirm(translate(message));
  window.alert = message => nativeAlert(translate(message));

  document.documentElement.lang = 'en';
  translateTree(document);
  new MutationObserver(records => {
    for (const record of records) {
      if (record.type === 'characterData') translateTree(record.target);
      for (const node of record.addedNodes) translateTree(node);
    }
  }).observe(document.documentElement, {subtree: true, childList: true, characterData: true});
